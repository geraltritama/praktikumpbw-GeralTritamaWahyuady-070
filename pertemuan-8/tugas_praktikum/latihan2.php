<h2>Soal 2: Mencetak Bilangan Genap (For Loop)</h2>
<p>Program ini menggunakan perulangan <code>for</code> untuk mencetak deret bilangan genap antara 2 hingga 10.</p>
<hr>

<h3>Hasil Eksekusi:</h3>
<div class="hasil-box">
    <ul>
    <?php
    // Implementasi For Loop
    for ($i = 2; $i <= 10; $i++) {
        // pengecekan bilangan
        if ($i % 2 == 0) {
            echo "<li>Bilangan genap: <strong>$i</strong></li>";
        }
    }
    ?>
    </ul>
    <p><em>:D</em></p>
</div>
